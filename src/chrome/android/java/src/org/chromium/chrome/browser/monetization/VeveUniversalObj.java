package org.chromium.chrome.browser.monetization;

public class VeveUniversalObj {

    public String imgUrl;
    public String url;
    public String title;
    public String description;
    public String brand;
    public String impurl;

    public VeveUniversalObj(String imgUrl, String url, String title, String description, String brand, String impurl) {
        this.imgUrl = imgUrl;
        this.url = url;
        this.title = title;
        this.description = description;
        this.brand = brand;
        this.impurl = impurl;
    }
}
